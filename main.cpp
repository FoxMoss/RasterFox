FoxCamera *camera;
int main() {
  InitWindow(450, 450, "Window");
  camera = new FoxCamera(SCALE, WIDTH, WIDTH);
  camera->cameraPosition = {0, 0, 2};
  SetTargetFPS(60);

#if defined(PLATFORM_WEB)
  emscripten_set_main_loop(UpdateDrawFrame, 60, 1);
#else

  SetTargetFPS(60);

  while (!WindowShouldClose()) {
    UpdateDrawFrame();
  }
#endif
}

static void UpdateDrawFrame(void) {
  Vector3 nextPos = Vector3Zero();
  if (IsKeyDown(KEY_E)) {
    nextPos.y -= STEP;
  }
  if (IsKeyDown(KEY_Q)) {
    nextPos.y += STEP;
  }
  if (IsKeyDown(KEY_A)) {
    nextPos.x -= STEP;
  }
  if (IsKeyDown(KEY_D)) {
    nextPos.x += STEP;
  }
  if (IsKeyDown(KEY_S)) {
    nextPos.z -= STEP;
  }
  if (IsKeyDown(KEY_W)) {
    nextPos.z += STEP;
  }

  if (IsKeyDown(KEY_LEFT)) {
    camera->cameraTheta.y -= SENSITIVITY;
  }
  if (IsKeyDown(KEY_RIGHT)) {
    camera->cameraTheta.y += SENSITIVITY;
  }

  nextPos = RotateAxis(nextPos, AXIS_Y, -camera->cameraTheta.y);
  camera->cameraPosition = Vector3Add(camera->cameraPosition, nextPos);

  ClearBackground(BLACK);

  for (Tri currentPoint : points) {
    camera->DrawTri(currentPoint);
  }

  camera->Draw();

  DrawFPS(10, 10);

  EndDrawing();
}
